# Diferencias entre Python y JavaScript:


#  Python:


# 1. Propósito General: 

#    - Python es un lenguaje de programación de alto nivel, interpretado y de propósito general. Esto significa que puede utilizarse para una amplia variedad de tareas, desde desarrollo web hasta ciencia de datos, aprendizaje automático e incluso scripting del sistema operativo.

# 2. Sintaxis:

#    - Python se destaca por su sintaxis clara y legible, lo que facilita su aprendizaje y uso. Emplea indentación para definir bloques de código, en lugar de llaves como otros lenguajes.

# 3. Tipado Dinámico:

#    - Python es un lenguaje de tipado dinámico, lo que significa que no es necesario declarar el tipo de una variable antes de usarla. El intérprete lo deduce en tiempo de ejecución.

# 4. Ecosistema:

#    - Cuenta con una vasta biblioteca estándar y una comunidad activa que ha desarrollado una gran cantidad de módulos y paquetes para casi cualquier tarea imaginable.




#   JavaScript:


# 1. Desarollo Web:

#    - JavaScript está diseñado principalmente para crear páginas web interactivas. Se ejecuta directamente en el navegador web y permite manipular el DOM (Document Object Model) para modificar el contenido y el estilo de una página web.

# 2. Event-Driven:

#    - JavaScript es un lenguaje orientado a eventos. Esto significa que el código se ejecuta en respuesta a eventos, como clics del mouse, pulsaciones de teclas o cambios en el contenido de la página.

# 3. Tipado Débil:

#    - JavaScript tiene un tipado débil, lo que significa que es menos estricto en cuanto a la verificación de tipos de datos. Esto puede llevar a errores en tiempo de ejecución si no se tiene cuidado.

# 4. Entorno de Ejecución:

#    - JavaScript se ejecuta en un entorno de ejecución llamado motor JavaScript, que forma parte del navegador web.

# Datos importantes: 

# Strings:

#    - En Python, el código fuente es ASCII por defecto a menos que especifiquemos algún formato de codificación.

#    - JavaScript debe codificarse como UTF-16 y no hay soporte incorporado para manipular bytes.

